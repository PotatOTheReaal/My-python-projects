def main():
    num1=float(input("Type number 1 : "))
    num2=float(input("Type number 2 : "))
    sign=str(input("choose you signe : "))
    if sign=="+":
        print(num1+num2)
    elif sign=="-":
        print(num1-num2)
    elif sign=="*":
        print(num1 * num2)
    elif sign=="/":
        print(num1/num2)

main()



